package base.entity;

public class Manufacturer {
    private int id;
    private String manufacturer;

    public int getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getManufacturer() {
        return manufacturer;
    }

    public void setManufacturer(String manufacturers) {
        this.manufacturer = manufacturers;
    }
}
